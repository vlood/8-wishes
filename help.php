<a name="addCategory"></a><b>What are categories and how do you add them</b>
</font>
<hr size=2 width="100%" align=center>
</td></tr>
<tr><td align=left>

You can use Categories to group similar items together.  For instance,
you could have a Book category and a DVD category.  In order to add a
new category, goto <font color=navy>Modify Your List</font> and click
on <input type=button value="Add New Category">.  In the page that
opens you will have the following options
<ul>
<li><b>Category Name:</b> enter a name for the category (e.g. Books)

<li><b>Category Description:</b> enter a description for the category
(e.g. I prefer hard cover books)

<li><b>Link Name & URL:</b> use these fields if you want to associate
a url for this category.  For example, if all the items in the
category will come from one store such as L.L. Bean, then you could
provide a link to the L.L. Bean website.

<p>In <b>Link Name</b> supply a name for the link (e.g. L.L. Bean)

<br>In <b>URL</b>, supply the url for the link
(e.g. http://www.llbean.com) 

<li><b>Additional Information:</b> use this field to add more
information about the category.  For example, if the category was
Books then you could use this field to indicate that you don't mind
receiving used books as long as they are in good condition.  This
field will show up on a seprate line.
</ul>
<p>When you are finished editing the fields, click the <input type=button value="Add Category"> button

<p>
<b>Note</b>:  There is a special category call <font color=navy>"Items Under Consideration"</font>. Items you place in this category will not be visible to the people who can view your list so you can use it to store items you are considering for inclusion. If you decide you do want an item in this category to be visible to everyone else, simply move it to another category (i.e. click <font color=navy>[edit]</font> for the item you want to move and then select the desired category in the drop down box)

<p>
