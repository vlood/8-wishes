<a name="addItem"></a><b>How to add items to your list</b>
</font>
<hr size=2 width="100%" align=center>
</td></tr> <tr><td align=left>
 Goto <font color=navy>Modify Your
List</font>.  First determine which category in which to place the
item (e.g. Books, DVD's, etc).  If the category does not exist yet,
you will need to add it.  Click <a href="">here</a> for information on
how to do this.
<p>
At the bottom of each category there is a link called <b><font color=navy>Add New Item to <i>Category Name</i></font></b>.  Click on that link.  In the page that opens you have the following fields
<ul>
<li><b>Title</b>: Enter the name of the item (e.g Gone With the Wind).
<li><b>Description</b>: Enter a brief description of the item (e.g. a novel by Margret Mitchell)
<li><b>Price</b>: a representative price for the item
<li><b>Quantity</b>: the amount of this item that you want

<li><b>Link 1 Name & URL</b>: here you can provide a link to a website
which is selling the item you are adding.  
<p>
If the item is availabe from Amazon, then you can simply cut and paste
the Amazon url into the URL textbox and click <b><font
color=navy>Perform Amazon Lookup</font></b>.  The WishList Site will
then fetch all the information about that item from Amazon and
auto-populate the <b>Title</b>, <b>Price</b>, <b>Quantity</b>, and
<b>Link 1 Name</b> for you.
<p>
If the site is not Amazon, the provide a name for the link (e.g. Best Buy) and copy and paste the url into the URL textbox

<li><b>Additional Information:</b> here you can add more information
about the item.  This field will appear on a seperate line.
<li><b>Link 2 Name & URL:</b> here you can provide an alternate web site where the item can be bought
<li><b>Link 3 Name & URL:</b> same idea as Link 2
<li><b>Allow people to check this item off:</b> If this box is checked then people can indicate they have bought it (i.e. a checkbox or drop down list will appear next to that item).  If you have items that you don't want people to check off (e.g. Gift Certificates) then uncheck this box
<li><b>Add a star next to this item:</b> if this box is checked then a <img src="images/star.gif"> will appear next to it.  The star indicates that you really want the item.
</ul>
<p>
After you are done filling in the fields, click the <b><font color=navy>Add</font></b> button
